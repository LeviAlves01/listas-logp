valor = int(input("Digite o valor:"))

if(valor%2==0):
    print("Esse valor é par")
else:
    print("Esse valor é ímpar")

# O programa solicita ao usuário que entre com um valor inteiro. O algoritmo verifica se o valor é par ou impar através do comando %, o qual informa se o resto da divisão por 2 é 0, indicando que o valor é par, ou maior que 0, indicando que o valor é ímpar.
numero = int(input("Digite o número do curso:"))

if(numero==1):
    print("Esse número corresponde ao curso de Engenharia")
elif(numero==2):
    print("Esse número corresponde ao curso de Edificações")
elif(numero==3):
    print("Esse número corresponde ao curso de Sistemas Elétricos")
elif(numero==4):
    print("Esse número corresponde ao curso de Turismo")
elif(numero==5):
    print("Esse número corresponde ao curso de Análise de Sistemas")
else:
    print("Esse número não corresponde a nenhum curso")